public class swap {
    public static void main(String[]args){
        int a=10;
        int b=60;
        int temp;
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swapping:");
        System.out.println("enter a value:"+a);
        System.out.println("enter b value:"+b);
        }
    
}
